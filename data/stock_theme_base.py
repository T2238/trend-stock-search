def get_theme_weight(code: str, theme_name: str) -> float:
    """
    銘柄コードとテーマ名から重みを返す
    マスター未登録の場合は None（セクターコードベースの重み 1.0 を使う）
    """
    stock_weights = STOCK_THEME_WEIGHTS.get(str(code))
    if stock_weights is None:
        return None
    return stock_weights.get(theme_name)


def get_all_themes_for_stock(code: str) -> dict[str, float]:
    """銘柄コードに紐づく全テーマ重みを返す"""
    return STOCK_THEME_WEIGHTS.get(str(code), {})
